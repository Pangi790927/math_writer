#ifndef MATH_VARIABLE_H
#define MATH_VARIABLE_H

/*! Math Variable
 * 
 * Building block for expressions. Those are the raw elements of the expression. Constants are also
 * included here as "static" variables. Names and morphing to other variables are taken care here.
 * example: a = 3 and b = a + 2 implies or results in b = 2 + 3 and further (via an formula) b = 5.
 * Even though the sum was done via an formula acting on things marked as constants (2 and 3) the
 * substitution a -> 3 was done via the system implemented here.
 */

#endif
